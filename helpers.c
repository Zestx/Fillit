#include "fillit.h"

int		compare_coords(int crd[4][2], int i, int j)
{
	if (crd[i][0] == crd[j][0] &&
			(crd[i][1] == crd[j][1] + 1 || crd[i][1] == crd[j][1] - 1))
		return (1);
	if (crd[i][1] == crd[j][1] &&
			(crd[i][0] == crd[j][0] + 1 || crd[i][0] == crd[j][0] - 1))
		return (1);
	return (0);
}
